#include "util.h"

void	ft_debug(void (*f)(void *), void *param)
{
	if (f == NULL || DEBUG == 0)
		return ;
	ft_putstr_fd("======================= DEBUG ========================\n", 1);
	f(param);
	ft_putstr_fd("=======================  END  ========================\n", 1);
}
